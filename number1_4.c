#include <stdio.h>      // Подключение библиотеки для ввода/вывода (printf, scanf)
#include <string.h>     // Подключение библиотеки для работы со строками (strcpy, strcat)
#include <locale.h>     // Подключение библиотеки для русской локализации
#include <windows.h>    // Подключение библиотеки Windows для настройки консоли

#define N 2             // Определение константы N со значением 2 (аналог Const N = 2;)

int K = 5;              // Объявление и инициализация глобальной переменной K (аналог K: byte=5;)

int X, Y;               // Объявление глобальных переменных X и Y типа int (аналог Var X, Y: integer;)
long Z;                 // Объявление глобальной переменной Z типа long (аналог z: longint;)
float R;                // Объявление глобальной переменной R типа float (аналог r: real;)
char S[26];             // Объявление глобальной переменной S типа char массив на 25 символов + 1 (аналог s: string[25];)

void f1() {             // Объявление процедуры f1 (аналог Procedure f1;)
    Y = !X;             // Логическое НЕ: если X=0 то Y=1, если X≠0 то Y=0 (аналог y := not x;)
    strcat(S, S);       // Конкатенация строки с самой собой (удвоение) (аналог s := s + s;)
}                       // Конец процедуры f1

long f2() {             // Объявление функции f2 возвращающей значение типа long (аналог function f2:longint;)
    X = (X & 0xFF) | K; // Побитовое И с 255 (0xFF) и затем побитовое ИЛИ с K (аналог x := x and $FF or k;)
    return X ^ Y;       // Возврат побитового исключающего ИЛИ X и Y (аналог f2 := x xor y;)
}                       // Конец функции f2

void f3(float *w) {     // Объявление процедуры f3 с параметром w указателем (аналог Procedure f3 (Var w:real);)
    *w = 2.0 * R;       // Присваивание w значения 2.0 * R (аналог w := 2.0*R;)
    strcpy(S, "Еще строка");  // Копирование строки в S (аналог s := 'Еще строка';)
    printf("s=%s\n", S);      // Вывод строки S (аналог writeln('s=', s);)
}                       // Конец процедуры f3

int main() {            // Главная функция программы (аналог begin ... end. в Паскале)
    SetConsoleCP(1251);         // Установка кодовой страницы 1251 для ввода русского текста
    SetConsoleOutputCP(1251);   // Установка кодовой страницы 1251 для вывода русского текста
    setlocale(LC_ALL, "Russian"); // Установка русской локали для корректного отображения
    
    system("cls");      // Очистка экрана консоли (аналог Clrscr;)
    
    printf("Введите x-> ");     // Вывод приглашения для ввода X (аналог write('Введите x->');)
    scanf("%d", &X);            // Ввод значения X с клавиатуры (аналог Readln(x);)
    
    f1();               // Вызов процедуры f1 (аналог f1;)
    
    Z = f2();           // Вызов функции f2 и присваивание результата переменной Z (аналог z := f2;)
    
    strcpy(S, "Строка"); // Копирование строки в S (аналог S := 'Строка';)
    
    f3(&R);             // Вызов процедуры f3 с передачей адреса переменной R (аналог f3(r);)
    
    printf("x=%5d\n", X);   // Вывод значения X с шириной поля 5 (аналог writeln('x=', x:5);)
    
    printf("r=%5.2f\n", R); // Вывод R с 2 знаками после запятой и шириной 5 (аналог writeln('r=', r:5:2);)
    
    system("pause");    // Команда для приостановки выполнения программы (ожидание нажатия любой клавиши)
    return 0;           // Возврат 0 в операционную систему (успешное завершение программы)
}                       // Конец главной функции
