#include <stdio.h>      // Подключение библиотеки для ввода/вывода (printf, scanf)
#include <string.h>     // Подключение библиотеки для работы со строками (strcpy, strcmp)
#include <locale.h>     // Подключение библиотеки для русской локализации
#include <windows.h>    // Подключение библиотеки Windows для настройки консоли

struct T {              // Объявление структуры T (аналог type T = record)
    int A;              // Поле A типа int (аналог A: integer;)
    float B;            // Поле B типа float (аналог B: real;)
    char S[100];        // Поле S типа char массив (аналог S: string;)
};                      // Конец структуры (аналог end;)

struct T r;             // Объявление переменной r типа struct T (аналог Var r: t;)

void P(struct T *q) {   // Объявление процедуры P с параметром q указателем на структуру (аналог Procedure P(Var Q: t);)
    int i1;             // Объявление локальной переменной i1 типа int (аналог Var i1: integer;)
    float i2;           // Объявление локальной переменной i2 типа float (аналог i2: real;)
    char i3[100];       // Объявление локальной переменной i3 типа char массив (аналог i3: string;)
    
    printf("Введите целое число -> ");  // Вывод приглашения для ввода целого числа (аналог write('Введите целое число ->');)
    scanf("%d", &i1);   // Ввод целого числа с клавиатуры (аналог readln(i1);)
    q->A = i1;          // Присваивание полю A структуры введенного значения (аналог r.a := i1;)
    
    printf("Введите вещ. число -> ");   // Вывод приглашения для ввода вещественного числа (аналог write('Введите вещ. число ->');)
    scanf("%f", &i2);   // Ввод вещественного числа с клавиатуры (аналог readln(i2);)
    q->B = i2;          // Присваивание полю B структуры введенного значения (аналог q.b := i2;)
    
    printf("Введите строку -> ");       // Вывод приглашения для ввода строки (аналог write('Введите строку ->');)
    scanf("%s", i3);    // Ввод строки с клавиатуры (аналог readln(i3);)
    strcpy(r.S, i3);    // Копирование строки в поле S структуры (аналог r.s := i3;)
    
    printf("\n");       // Вывод пустой строки (аналог writeln;)
    getchar();          // Очистка буфера ввода (аналог readln;)
    getchar();          // Ожидание нажатия клавиши (аналог readln;)
}                       // Конец процедуры P (аналог end;)

float f(float j1, float j2) {   // Объявление функции f с двумя параметрами типа float (аналог Function f(j1: real; j2: real): real;)
    return (j1 + j2) / 2;       // Возврат среднего арифметического двух чисел (аналог f := (j1+j2)/2;)
}                               // Конец функции f

int main() {            // Главная функция программы (аналог begin ... end. в Паскале)
    SetConsoleCP(1251);         // Установка кодовой страницы 1251 для ввода русского текста
    SetConsoleOutputCP(1251);   // Установка кодовой страницы 1251 для вывода русского текста
    setlocale(LC_ALL, "Russian"); // Установка русской локали для корректного отображения
    
    float x, y, z;      // Объявление локальных переменных x, y, z типа float (аналог Var x, y, z: real;)
    
    system("cls");      // Очистка экрана консоли (аналог clrscr;)
    
    P(&r);              // Вызов процедуры P с передачей адреса структуры r (аналог P(r);)
    
    printf("a=%5d b=%5.2f s=%s\n", r.A, r.B, r.S);  // Вывод полей структуры (аналог write('a=', r.a:5, ' b=', q.b:5:2, ' s=', r.s:2); writeln;)
    
    printf("Введите x-> ");     // Вывод приглашения для ввода x (аналог write('Введите x->');)
    scanf("%f", &x);            // Ввод значения x с клавиатуры (аналог Readln(x);)
    
    printf("Введите y-> ");     // Вывод приглашения для ввода y (аналог write('Введите y->');)
    scanf("%f", &y);            // Ввод значения y с клавиатуры (аналог Readln(y);)
    
    z = f(x, y);                // Вызов функции f и присваивание результата переменной z (аналог z := f(x,y);)
    
    printf("Результат = %6.2f\n", z);  // Вывод результата (аналог writeln('Результат = ', z:6:2);)
    
    system("pause");    // Команда для приостановки выполнения программы (ожидание нажатия любой клавиши)
    return 0;           // Возврат 0 в операционную систему (успешное завершение программы)
}                       // Конец главной функции
