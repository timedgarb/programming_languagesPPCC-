#include <iostream>     // Подключение библиотеки для ввода/вывода (cout, cin)
#include <iomanip>      // Подключение библиотеки для форматированного вывода (setw, setprecision)
#include <string>       // Подключение библиотеки для работы со строками (string)
#include <clocale>      // Подключение библиотеки для русской локализации
#include <windows.h>    // Подключение библиотеки Windows для настройки консоли
using namespace std;    // Использование стандартного пространства имен

struct T {              // Объявление структуры T (аналог type T = record)
    int A;              // Поле A типа int (аналог A: integer;)
    double B;           // Поле B типа double (аналог B: real;)
    string S;           // Поле S типа string (аналог S: string;)
};                      // Конец структуры (аналог end;)

T r;                    // Объявление переменной r типа T (аналог Var r: t;)

void P(T &q) {          // Объявление процедуры P с параметром q типа T передаваемым по ссылке (аналог Procedure P(Var Q: t);)
    int i1;             // Объявление локальной переменной i1 типа int (аналог Var i1: integer;)
    double i2;          // Объявление локальной переменной i2 типа double (аналог i2: real;)
    string i3;          // Объявление локальной переменной i3 типа string (аналог i3: string;)
    
    cout << "Введите целое число -> ";  // Вывод приглашения для ввода целого числа (аналог write('Введите целое число ->');)
    cin >> i1;          // Ввод целого числа с клавиатуры (аналог readln(i1);)
    q.A = i1;           // Присваивание полю A структуры введенного значения (аналог r.a := i1;)
    
    cout << "Введите вещ. число -> ";   // Вывод приглашения для ввода вещественного числа (аналог write('Введите вещ. число ->');)
    cin >> i2;          // Ввод вещественного числа с клавиатуры (аналог readln(i2);)
    q.B = i2;           // Присваивание полю B структуры введенного значения (аналог q.b := i2;)
    
    cout << "Введите строку -> ";       // Вывод приглашения для ввода строки (аналог write('Введите строку ->');)
    cin.ignore();       // Очистка буфера ввода от символа новой строки
    getline(cin, i3);   // Ввод строки с пробелами с клавиатуры (аналог readln(i3);)
    r.S = i3;           // Присваивание полю S структуры введенной строки (аналог r.s := i3;)
    
    cout << endl;       // Вывод пустой строки (аналог writeln;)
}                       // Конец процедуры P (аналог end;)

double f(double j1, double j2) {  // Объявление функции f с двумя параметрами типа double (аналог Function f(j1: real; j2: real): real;)
    return (j1 + j2) / 2;         // Возврат среднего арифметического двух чисел (аналог f := (j1+j2)/2;)
}                                 // Конец функции f

int main() {            // Главная функция программы (аналог begin ... end. в Паскале)
    SetConsoleCP(1251);         // Установка кодовой страницы 1251 для ввода русского текста
    SetConsoleOutputCP(1251);   // Установка кодовой страницы 1251 для вывода русского текста
    setlocale(LC_ALL, "Russian"); // Установка русской локали для корректного отображения
    
    double x, y, z;     // Объявление локальных переменных x, y, z типа double (аналог Var x, y, z: real;)
    
    system("cls");      // Очистка экрана консоли (аналог clrscr;)
    
    P(r);               // Вызов процедуры P с передачей структуры r по ссылке (аналог P(r);)
    
    cout << "a=" << setw(5) << r.A           // Вывод поля A с шириной 5 (аналог write('a=', r.a:5)
         << " b=" << fixed << setprecision(2) << setw(5) << r.B  // Вывод поля B с 2 знаками после запятой (аналог ' b=', q.b:5:2)
         << " s=" << r.S << endl;            // Вывод поля S и переход на новую строку (аналог ' s=', r.s:2);)
    
    cout << "Введите x-> ";     // Вывод приглашения для ввода x (аналог write('Введите x->');)
    cin >> x;                   // Ввод значения x с клавиатуры (аналог Readln(x);)
    
    cout << "Введите y-> ";     // Вывод приглашения для ввода y (аналог write('Введите y->');)
    cin >> y;                   // Ввод значения y с клавиатуры (аналог Readln(y);)
    
    z = f(x, y);                // Вызов функции f и присваивание результата переменной z (аналог z := f(x,y);)
    
    cout << "Результат = " << fixed << setprecision(2) << setw(6) << z << endl;  // Вывод результата (аналог writeln('Результат = ', z:6:2);)
    
    system("pause");    // Команда для приостановки выполнения программы (ожидание нажатия любой клавиши)
    return 0;           // Возврат 0 в операционную систему (успешное завершение программы)
}                       // Конец главной функции