#include <iostream>     // Подключение библиотеки для ввода/вывода (cout, cin)
#include <iomanip>      // Подключение библиотеки для форматированного вывода (setw, setprecision)
#include <clocale>      // Подключение библиотеки для русской локализации
#include <windows.h>    // Подключение библиотеки Windows для настройки консоли
using namespace std;    // Использование стандартного пространства имен

const int n = 2;        // Объявление константы n типа int со значением 2 (аналог const n=2;)
const double m = 2.0;   // Объявление константы m типа double со значением 2.0 (аналог const m=2.0;)

int x, y, z;            // Объявление глобальных переменных x, y, z типа int (аналог Var x, y, z: integer;)
double r;               // Объявление глобальной переменной r типа double (аналог r: real;)

void P1(int &A) {       // Объявление процедуры P1 с параметром A передаваемым по ссылке (аналог Procedure P1(Var A: integer);)
    A = x * n - y;      // Присваивание A значения x*n - y (аналог a := x*n - y;)
}                       // Конец процедуры P1

double f1() {           // Объявление функции f1 возвращающей значение типа double (аналог Function f1:real;)
    return y * m + x;   // Возврат значения y*m + x (аналог f1 := y*m + x;)
}                       // Конец функции f1

int main() {            // Главная функция программы (аналог begin ... end. в Паскале)
    SetConsoleCP(1251);         // Установка кодовой страницы 1251 для ввода русского текста
    SetConsoleOutputCP(1251);   // Установка кодовой страницы 1251 для вывода русского текста
    setlocale(LC_ALL, "Russian"); // Установка русской локали для корректного отображения
    
    cout << "x=";       // Вывод приглашения для ввода x (аналог write('x=');)
    cin >> x;           // Ввод значения x с клавиатуры (аналог readln(x);)
    
    cout << "y=";       // Вывод приглашения для ввода y (аналог write('y=');)
    cin >> y;           // Ввод значения y с клавиатуры (аналог readln(y);)
    
    y = !x;             // Логическое НЕ: если x=0 то y=1, если x≠0 то y=0 (аналог Y:=not x;)
    
    x = y ^ 0xF;        // Побитовое исключающее ИЛИ с числом 15 (0xF) (аналог X:= y xor $F;)
    
    P1(z);              // Вызов процедуры P1 с передачей переменной z по ссылке (аналог p1(z);)
    
    cout << "z=" << setw(5) << z << endl;  // Вывод значения z с шириной поля 5 и переход на новую строку (аналог writeln('z=', z:5);)
    
    r = f1();           // Вызов функции f1 и присваивание результата переменной r (аналог R:=f1;)
    
    cout << "r=" << fixed << setprecision(2) << setw(5) << r << endl;  // Вывод r с 2 знаками после запятой и шириной 5 (аналог writeln('r=', r:5:2);)
    
    system("pause");    // Команда для приостановки выполнения программы (ожидание нажатия любой клавиши)
    return 0;           // Возврат 0 в операционную систему (успешное завершение программы)
}                       // Конец главной функции